/**
 * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
 * PROGRESS MANAGER - –£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –ø—Ä–æ–≥—Ä–µ—Å—Å–æ–º –∏–≥—Ä–æ–∫–∞
 * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
 */

class ProgressManager {
    constructor() {
        this.userId = null;
        
        // –ü–æ–¥–ø–∏—Å—ã–≤–∞–µ–º—Å—è –Ω–∞ –∏–∑–º–µ–Ω–µ–Ω–∏—è –∞—É—Ç–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ü–∏–∏
        if (window.authManager) {
            authManager.onAuthStateChanged(user => {
                this.userId = user ? user.uid : null;
            });
        }
        
        console.log('üìä ProgressManager –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω');
    }
    
    /**
     * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
     * –°–û–•–†–ê–ù–ï–ù–ò–ï –†–ï–ó–£–õ–¨–¢–ê–¢–ê –ò–ì–†–´
     * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
     */
    
    async saveGameResult(themeId, difficulty, results) {
        if (!this.userId) {
            console.log('‚ö†Ô∏è –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –≤–æ—à—ë–ª - –ø—Ä–æ–≥—Ä–µ—Å—Å –Ω–µ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç—Å—è');
            return;
        }
        
        try {
            console.log('üíæ –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞:', { themeId, difficulty, results });
            
            const progressRef = db.collection('users')
                .doc(this.userId)
                .collection('progress')
                .doc(`${themeId}_${difficulty}`);
            
            // –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–∏–π –ø—Ä–æ–≥—Ä–µ—Å—Å
            const doc = await progressRef.get();
            const currentData = doc.exists ? doc.data() : {};
            
            // –û–±–Ω–æ–≤–ª—è–µ–º –µ—Å–ª–∏ —ç—Ç–æ –ª—É—á—à–∏–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç
            const updates = {
                themeId: themeId,
                difficulty: difficulty,
                timesPlayed: (currentData.timesPlayed || 0) + 1,
                lastPlayed: firebase.firestore.FieldValue.serverTimestamp()
            };
            
            if (!currentData.bestScore || results.score > currentData.bestScore) {
                updates.bestScore = results.score;
            }
            
            if (!currentData.bestAccuracy || results.accuracy > currentData.bestAccuracy) {
                updates.bestAccuracy = results.accuracy;
            }
            
            if (!currentData.bestCombo || results.combo > currentData.bestCombo) {
                updates.bestCombo = results.combo;
            }
            
            if (results.completed) {
                updates.completed = true;
                updates.completedAt = firebase.firestore.FieldValue.serverTimestamp();
            }
            
            await progressRef.set(updates, { merge: true });
            
            // –û–±–Ω–æ–≤–ª—è–µ–º –æ–±—â—É—é —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
            await this.updateStats(results);
            
            console.log('‚úÖ –ü—Ä–æ–≥—Ä–µ—Å—Å —Å–æ—Ö—Ä–∞–Ω—ë–Ω');
            
        } catch (error) {
            console.error('‚ùå –û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –ø—Ä–æ–≥—Ä–µ—Å—Å–∞:', error);
        }
    }
    
    /**
     * –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –æ–±—â–µ–π —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
     */
    async updateStats(results) {
        const userRef = db.collection('users').doc(this.userId);
        
        await userRef.update({
            'stats.totalGames': firebase.firestore.FieldValue.increment(1),
            'stats.totalCorrect': firebase.firestore.FieldValue.increment(results.correct || 0),
            'stats.totalIncorrect': firebase.firestore.FieldValue.increment(results.incorrect || 0),
            'stats.totalCombo': firebase.firestore.FieldValue.increment(results.combo || 0),
            'stats.totalPlayTime': firebase.firestore.FieldValue.increment(results.duration || 0)
        });
    }
    
    /**
     * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
     * –ü–û–õ–£–ß–ï–ù–ò–ï –ü–†–û–ì–†–ï–°–°–ê
     * ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
     */
    
    /**
     * –ü–æ–ª—É—á–∏—Ç—å –ø—Ä–æ–≥—Ä–µ—Å—Å –ø–æ —Ç–µ–º–µ
     * @param {string} themeId - ID —Ç–µ–º—ã
     * @param {number} difficulty - –°–ª–æ–∂–Ω–æ—Å—Ç—å (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ, –µ—Å–ª–∏ —É–∫–∞–∑–∞–Ω–∞ - –≤–µ—Ä–Ω—ë—Ç —Ç–æ–ª—å–∫–æ –¥–ª—è —ç—Ç–æ–π —Å–ª–æ–∂–Ω–æ—Å—Ç–∏)
     */
    async getThemeProgress(themeId, difficulty = null) {
        if (!this.userId) return difficulty !== null ? null : {};
        
        try {
            let query = db.collection('users')
                .doc(this.userId)
                .collection('progress')
                .where('themeId', '==', themeId);
            
            // –ï—Å–ª–∏ —É–∫–∞–∑–∞–Ω–∞ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å
            if (difficulty !== null) {
                const docId = `${themeId}_${difficulty}`;
                const doc = await db.collection('users')
                    .doc(this.userId)
                    .collection('progress')
                    .doc(docId)
                    .get();
                
                return doc.exists ? doc.data() : null;
            }
            
            // –ò–Ω–∞—á–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≤—Å–µ —Å–ª–æ–∂–Ω–æ—Å—Ç–∏
            const snapshot = await query.get();
            
            const progress = {};
            snapshot.forEach(doc => {
                const data = doc.data();
                progress[data.difficulty] = data;
            });
            
            return progress;
            
        } catch (error) {
            console.error('‚ùå –û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –ø—Ä–æ–≥—Ä–µ—Å—Å–∞:', error);
            return difficulty !== null ? null : {};
        }
    }
    
    async getAllProgress() {
        if (!this.userId) return [];
        
        try {
            const snapshot = await db.collection('users')
                .doc(this.userId)
                .collection('progress')
                .get();
            
            const progress = [];
            snapshot.forEach(doc => {
                progress.push(doc.data());
            });
            
            return progress;
            
        } catch (error) {
            console.error('‚ùå –û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –ø—Ä–æ–≥—Ä–µ—Å—Å–∞:', error);
            return [];
        }
    }
    
    async getStats() {
        if (!this.userId) return null;
        
        try {
            const userDoc = await db.collection('users')
                .doc(this.userId)
                .get();
            
            return userDoc.exists ? userDoc.data().stats : null;
            
        } catch (error) {
            console.error('‚ùå –û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏:', error);
            return null;
        }
    }
}

// –°–æ–∑–¥–∞—ë–º –≥–ª–æ–±–∞–ª—å–Ω—ã–π —ç–∫–∑–µ–º–ø–ª—è—Ä
window.progressManager = new ProgressManager();
